package ex04_Operators;

public class Lab039_Operators_Modules {
    public static void main(String[] args) {

        int a = 10;
        int b = 3;
        int c = a/b; //result is division
        int d = a%b; // result = remainder

        System.out.println(c);
        System.out.println(d);
        System.out.println(10%3);//remainder is 1


    }
}
